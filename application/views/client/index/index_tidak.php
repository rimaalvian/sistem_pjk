<h3>Sistem Deteksi Risiko Penyakit Jantung Koroner</h3>
<hr>
<p>Sistem ini merupakan sebuah sistem yang dapat digunakan untuk melakukan pengecekan tingkat risiko Penyakit Jantung Koroner. Untuk melakukan pengecekan, silahkan lakukan langkah-langkah berikut ini :</p>
<ol>
	<li>Memilih menu "Cek Risiko".</li>
	<li>Mengisi kolom-kolom yang tersedia.</li>
	<li>Memilih tombol "Cek".</li>
	<li>Memperoleh hasil.</li>
</ol>
<p>Anda akan memperoleh hasil berupa persentase, keterangan risiko, dan saran. Selamat mencoba dan selalu sayangi jantung Anda!</p>
